      <h3>14 NIETIGHEID</h3>
      <p
        >14.1 Als een bepaling (of een gedeelte daarvan) ongeldig, nietig of
        onafdwingbaar is of wordt, dan tast die ongeldige, nietige of
        onafdwingbare bepaling de overige bepalingen niet aan.</p
      >
      <p
        >14.2 Als een bepaling (of een gedeelte daarvan) ongeldig, nietig of
        onafdwingbaar is of wordt, dan zijn partijen gebonden aan een bepaling
        die wel geldig, niet nietig en wel afdwingbaar is, en die zoveel
        mogelijk een vergelijkbaar resultaat heeft als de oorspronkelijke
        bepaling.</p
      >

