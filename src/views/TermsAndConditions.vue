        >13.3 U zorgt ervoor dat wij ieder bedrag onder deze vrijwaring binnen
        10 werkdagen na ons schriftelijke verzoek hebben ontvangen. Waar wij dat
        nodig vinden zullen we ons verzoek onderbouwen.</p
      >
      <p
        >13.4 Het bepaalde in artikel 6:89 of artikel 7:23 van het Burgerlijk
        Wetboek is niet van toepassing ten aanzien van deze vrijwaring.</p
