        >13.1 Als u een ondernemer bent, vrijwaart u ons en stelt en houdt u ons
        volledig schadeloos tegen alle directe of indirecte acties, aanspraken,
        (rechts)vorderingen of (juridische) procedures van of door anderen
        (waaronder degenen die bij u een tegoedbon kopen of hebben gekocht,
        toezichthoudende autoriteiten en vergelijkbare organen, zoals de
        Belastingdienst) tegen ons, die voorvloeien uit of verband houden
        met:</p
      >
      <div>
        <p
          >13.1.1 uw eigen tekortkoming of onrechtmatige daad (waarbij het niet
          uitmaakt of die tekortkoming of onrechtmatige daad toerekenbaar is),
          waaronder een tekortkoming in de nakoming van enige verplichting in
          deze algemene voorwaarden of jegens degenen die bij u een tegoedbon
          kopen of hebben gekocht;</p
        >
        <p
          >13.1.2 uw gebruik, inzet, ter beschikking stelling (aan anderen) of
          aanbod van de Producten en Diensten of het niet voldoen aan of het
          onjuist opvolgen van onze eisen, instructies of voorwaarden
          betreffende de Producten en Diensten of het gebruik daarvan;</p
        >
        <p
          >13.1.3 enig handelen door u in strijd met enige wet- of regelgeving
          of enige (andere) verplichting of eis (waaronder enige verplichting
          die u heeft ten opzichte van iemand anders, zoals degenen die bij u
          een tegoedbon kopen of hebben gekocht).</p
        >
        <p
          >13.2 De vrijwaring omvat een vergoeding van alle kosten, verliezen,
          schade en ander nadeel (waaronder volledige juridische kosten en
          kosten van professionals die wij naar eigen goeddunken hebben
          ingeschakeld):</p
        >
        <p
          >13.2.1 waartoe wij zijn veroordeeld, of die wij hebben gemaakt of
          betaald, die voorvloeien uit of die verband houden met de acties,
          aanspraken, (rechts)vorderingen of juridische procedures als hierboven
          bedoeld in artikel 13.1, waaronder boetes en andere
          handhavingsmaatregelen en rente; en</p
        >
        <p
          >13.2.2 om nakoming door u van deze vrijwaring in of buiten rechte te
          vorderen en/of af te dwingen.</p
        >
      </div>
